class PetDetailModel {
  int id;
  String name;
  String species;
  String breed;
  int age;
  String location;
  String originCountry;
  int price;
  String height;
  String weight;
  String imageUrl;

  PetDetailModel({
    required this.id,
    required this.name,
    required this.species,
    required this.breed,
    required this.age,
    required this.location,
    required this.originCountry,
    required this.price,
    required this.height,
    required this.weight,
    required this.imageUrl,
  });
}
